/**
 * Recurring Expenses Service
 * Manages automatic generation of monthly/recurring expenses
 */

import { state } from '../utils/state.js';
import { Utils } from '../utils/utils.js';
import { db, collection, doc, addDoc, updateDoc, deleteDoc, getDocs, query, where, serverTimestamp } from '../config/firebase.js';
import { firebaseService } from './firebase-service.js';

class RecurringExpensesService {
    /**
     * Create a recurring expense template
     */
    async createRecurringExpense(data) {
        try {
            const recurringExpense = {
                description: data.description,
                category: data.category,
                amount: parseFloat(data.amount),
                frequency: data.frequency, // 'monthly', 'weekly', 'quarterly', 'yearly'
                dayOfMonth: parseInt(data.dayOfMonth) || 1,
                startDate: data.startDate,
                endDate: data.endDate || null, // null means indefinite
                isActive: true,
                lastGenerated: null,
                outletId: data.outletId || 'main',
                notes: data.notes || '',
                createdAt: new Date().toISOString(),
                userId: state.currentUser?.uid
            };

            const docRef = await addDoc(firebaseService.getUserCollection('recurring_expenses'), recurringExpense);
            Utils.showToast(`Recurring expense "${data.description}" created`, 'success');
            return { success: true, id: docRef.id };
        } catch (error) {
            console.error('Error creating recurring expense:', error);
            Utils.showToast('Failed to create recurring expense: ' + error.message, 'error');
            return { success: false, error: error.message };
        }
    }

    /**
     * Get all recurring expense templates
     */
    async getRecurringExpenses() {
        try {
            const snapshot = await getDocs(firebaseService.getUserCollection('recurring_expenses'));
            return snapshot.docs.map(doc => ({ id: doc.id, ...doc.data() }));
        } catch (error) {
            console.error('Error getting recurring expenses:', error);
            return [];
        }
    }

    /**
     * Check and generate due recurring expenses
     */
    async generateDueExpenses() {
        try {
            const recurringExpenses = await this.getRecurringExpenses();
            const today = new Date();
            const currentDay = today.getDate();
            const currentMonth = today.getMonth();
            const currentYear = today.getFullYear();
            let generated = 0;

            for (const recurring of recurringExpenses) {
                if (!recurring.isActive) continue;

                // Check if already generated this period
                const lastGenerated = recurring.lastGenerated ? new Date(recurring.lastGenerated) : null;
                const shouldGenerate = this.shouldGenerateExpense(recurring, today, lastGenerated);

                if (shouldGenerate) {
                    // Generate the actual expense
                    await addDoc(firebaseService.getUserCollection('expenses'), {
                        date: today.toISOString().split('T')[0],
                        description: recurring.description,
                        category: recurring.category,
                        amount: recurring.amount,
                        outletId: recurring.outletId,
                        recurringExpenseId: recurring.id,
                        autoGenerated: true,
                        notes: `Auto-generated from recurring expense: ${recurring.description}`,
                        createdAt: new Date().toISOString()
                    });

                    // Update last generated date
                    const recurringRef = doc(firebaseService.getUserCollection('recurring_expenses'), recurring.id);
                    await updateDoc(recurringRef, {
                        lastGenerated: today.toISOString()
                    });

                    generated++;
                }
            }

            if (generated > 0) {
                Utils.showToast(`${generated} recurring expense(s) generated`, 'info');
            }

            return generated;
        } catch (error) {
            console.error('Error generating recurring expenses:', error);
            return 0;
        }
    }

    /**
     * Determine if an expense should be generated
     */
    shouldGenerateExpense(recurring, today, lastGenerated) {
        const currentDay = today.getDate();
        const currentMonth = today.getMonth();
        const currentYear = today.getFullYear();

        // Check start date
        if (recurring.startDate && new Date(recurring.startDate) > today) {
            return false;
        }

        // Check end date
        if (recurring.endDate && new Date(recurring.endDate) < today) {
            return false;
        }

        // Check if it's the right day
        if (recurring.dayOfMonth !== currentDay) {
            return false;
        }

        // Check frequency and last generated
        if (lastGenerated) {
            const lastMonth = lastGenerated.getMonth();
            const lastYear = lastGenerated.getFullYear();

            switch (recurring.frequency) {
                case 'monthly':
                    // Don't generate if already generated this month
                    if (lastMonth === currentMonth && lastYear === currentYear) {
                        return false;
                    }
                    break;
                case 'quarterly':
                    // Generate every 3 months
                    const lastQuarter = Math.floor(lastMonth / 3);
                    const currentQuarter = Math.floor(currentMonth / 3);
                    if (lastQuarter === currentQuarter && lastYear === currentYear) {
                        return false;
                    }
                    break;
                case 'yearly':
                    if (lastYear === currentYear) {
                        return false;
                    }
                    break;
            }
        }

        return true;
    }

    /**
     * Toggle recurring expense active status
     */
    async toggleActive(id, isActive) {
        try {
            const recurringRef = doc(firebaseService.getUserCollection('recurring_expenses'), id);
            await updateDoc(recurringRef, { isActive });
            Utils.showToast(`Recurring expense ${isActive ? 'activated' : 'paused'}`, 'success');
            return true;
        } catch (error) {
            console.error('Error toggling recurring expense:', error);
            Utils.showToast('Failed to update: ' + error.message, 'error');
            return false;
        }
    }

    /**
     * Delete recurring expense
     */
    async deleteRecurring(id) {
        try {
            await deleteDoc(doc(firebaseService.getUserCollection('recurring_expenses'), id));
            Utils.showToast('Recurring expense deleted', 'success');
            return true;
        } catch (error) {
            console.error('Error deleting recurring expense:', error);
            Utils.showToast('Failed to delete: ' + error.message, 'error');
            return false;
        }
    }

    /**
     * Get summary of upcoming recurring expenses
     */
    async getUpcomingSummary() {
        const recurring = await this.getRecurringExpenses();
        const active = recurring.filter(r => r.isActive);
        
        const monthly = active.filter(r => r.frequency === 'monthly');
        const quarterly = active.filter(r => r.frequency === 'quarterly');
        const yearly = active.filter(r => r.frequency === 'yearly');

        const monthlyTotal = monthly.reduce((sum, r) => sum + r.amount, 0);
        const quarterlyMonthly = quarterly.reduce((sum, r) => sum + r.amount, 0) / 3;
        const yearlyMonthly = yearly.reduce((sum, r) => sum + r.amount, 0) / 12;

        return {
            total: active.length,
            monthly: monthly.length,
            quarterly: quarterly.length,
            yearly: yearly.length,
            estimatedMonthly: monthlyTotal + quarterlyMonthly + yearlyMonthly,
            nextExpenses: this.getNextDueExpenses(active)
        };
    }

    /**
     * Get next expenses due
     */
    getNextDueExpenses(recurring, limit = 5) {
        const today = new Date();
        const currentDay = today.getDate();
        
        return recurring
            .map(r => {
                let daysUntilDue = r.dayOfMonth - currentDay;
                if (daysUntilDue < 0) daysUntilDue += 30; // Approximate
                return { ...r, daysUntilDue };
            })
            .sort((a, b) => a.daysUntilDue - b.daysUntilDue)
            .slice(0, limit);
    }

    /**
     * Render recurring expenses modal
     */
    renderAddModal() {
        const modal = document.createElement('div');
        modal.id = 'add-recurring-expense-modal';
        modal.className = 'modal';
        modal.style.display = 'block';
        modal.innerHTML = `
            <div class="modal-content">
                <span class="close" onclick="document.getElementById('add-recurring-expense-modal').remove()">&times;</span>
                <h3><i class="fas fa-sync"></i> Add Recurring Expense</h3>
                
                <form id="add-recurring-form" onsubmit="recurringExpenses.handleAddSubmit(event)">
                    <label>Description *</label>
                    <input type="text" id="recurring-description" required placeholder="e.g., Monthly Rent">
                    
                    <label>Category *</label>
                    <select id="recurring-category" required>
                        <option value="">Select Category</option>
                        <option value="Rent">Rent</option>
                        <option value="Utilities">Utilities</option>
                        <option value="Salaries">Salaries</option>
                        <option value="Insurance">Insurance</option>
                        <option value="Subscriptions">Subscriptions</option>
                        <option value="Loan Payment">Loan Payment</option>
                        <option value="Other">Other</option>
                    </select>
                    
                    <label>Amount *</label>
                    <input type="number" id="recurring-amount" step="0.01" min="0.01" required placeholder="Amount">
                    
                    <label>Frequency *</label>
                    <select id="recurring-frequency" required>
                        <option value="monthly">Monthly</option>
                        <option value="quarterly">Quarterly</option>
                        <option value="yearly">Yearly</option>
                    </select>
                    
                    <label>Day of Month to Generate *</label>
                    <input type="number" id="recurring-day" min="1" max="28" value="1" required>
                    <small>Choose between 1-28 to ensure it generates every month</small>
                    
                    <label>Start Date *</label>
                    <input type="date" id="recurring-start" required value="${new Date().toISOString().split('T')[0]}">
                    
                    <label>End Date (Optional)</label>
                    <input type="date" id="recurring-end">
                    <small>Leave blank for indefinite recurring</small>
                    
                    <label>Notes</label>
                    <textarea id="recurring-notes" rows="2" placeholder="Additional notes..."></textarea>
                    
                    <button type="submit"><i class="fas fa-save"></i> Create Recurring Expense</button>
                </form>
            </div>
        `;

        document.body.appendChild(modal);
    }

    /**
     * Handle add form submission
     */
    async handleAddSubmit(event) {
        event.preventDefault();

        const data = {
            description: document.getElementById('recurring-description').value,
            category: document.getElementById('recurring-category').value,
            amount: document.getElementById('recurring-amount').value,
            frequency: document.getElementById('recurring-frequency').value,
            dayOfMonth: document.getElementById('recurring-day').value,
            startDate: document.getElementById('recurring-start').value,
            endDate: document.getElementById('recurring-end').value || null,
            notes: document.getElementById('recurring-notes').value
        };

        const result = await this.createRecurringExpense(data);
        
        if (result.success) {
            document.getElementById('add-recurring-expense-modal').remove();
            // Refresh expenses view
            if (window.appController) {
                window.appController.renderExpenses();
            }
        }
    }

    /**
     * Render recurring expenses table
     */
    async renderTable() {
        const recurring = await this.getRecurringExpenses();
        
        if (recurring.length === 0) {
            return `
                <div class="no-data-message">
                    <i class="fas fa-sync fa-3x" style="opacity: 0.3;"></i>
                    <p>No recurring expenses set up yet.</p>
                    <button onclick="recurringExpenses.renderAddModal()" class="btn-primary">
                        <i class="fas fa-plus"></i> Add Recurring Expense
                    </button>
                </div>
            `;
        }

        let html = `
            <div class="recurring-expenses-header" style="display: flex; justify-content: space-between; align-items: center; margin-bottom: 1rem;">
                <h4><i class="fas fa-sync"></i> Recurring Expenses</h4>
                <button onclick="recurringExpenses.renderAddModal()" class="btn-primary">
                    <i class="fas fa-plus"></i> Add New
                </button>
            </div>
            <div class="table-responsive">
                <table class="recurring-expenses-table">
                    <thead>
                        <tr>
                            <th>Description</th>
                            <th>Category</th>
                            <th>Amount</th>
                            <th>Frequency</th>
                            <th>Day</th>
                            <th>Status</th>
                            <th>Actions</th>
                        </tr>
                    </thead>
                    <tbody>
        `;

        recurring.forEach(r => {
            const statusClass = r.isActive ? 'status-active' : 'status-paused';
            html += `
                <tr>
                    <td>${r.description}</td>
                    <td>${r.category}</td>
                    <td>${Utils.formatCurrency(r.amount)}</td>
                    <td>${r.frequency}</td>
                    <td>${r.dayOfMonth}</td>
                    <td><span class="status-badge ${statusClass}">${r.isActive ? 'Active' : 'Paused'}</span></td>
                    <td>
                        <button onclick="recurringExpenses.toggleActive('${r.id}', ${!r.isActive})" 
                                class="btn-sm ${r.isActive ? 'btn-warning' : 'btn-success'}" 
                                title="${r.isActive ? 'Pause' : 'Activate'}">
                            <i class="fas fa-${r.isActive ? 'pause' : 'play'}"></i>
                        </button>
                        <button onclick="recurringExpenses.deleteRecurring('${r.id}')" 
                                class="btn-sm btn-danger" title="Delete">
                            <i class="fas fa-trash"></i>
                        </button>
                    </td>
                </tr>
            `;
        });

        html += '</tbody></table></div>';
        return html;
    }
}

export const recurringExpenses = new RecurringExpensesService();
window.recurringExpenses = recurringExpenses;
